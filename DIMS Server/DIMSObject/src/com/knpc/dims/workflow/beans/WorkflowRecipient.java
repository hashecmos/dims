package com.knpc.dims.workflow.beans;


public class WorkflowRecipient {
	private String workflowRecipient;
	private String workflowRecipientName;
	private String workflowWorkItemType;
	private String workflowWorkItemStatus;
	
	public String getWorkflowRecipient() {
		return workflowRecipient;
	}
	public void setWorkflowRecipient(String workflowRecipient) {
		this.workflowRecipient = workflowRecipient;
	}
	public String getWorkflowRecipientName() {
		return workflowRecipientName;
	}
	public void setWorkflowRecipientName(String workflowRecipientName) {
		this.workflowRecipientName = workflowRecipientName;
	}
	public String getWorkflowWorkItemType() {
		return workflowWorkItemType;
	}
	public void setWorkflowWorkItemType(String workflowWorkItemType) {
		this.workflowWorkItemType = workflowWorkItemType;
	}
	public String getWorkflowWorkItemStatus() {
		return workflowWorkItemStatus;
	}
	public void setWorkflowWorkItemStatus(String workflowWorkItemStatus) {
		this.workflowWorkItemStatus = workflowWorkItemStatus;
	}
	
	
}
